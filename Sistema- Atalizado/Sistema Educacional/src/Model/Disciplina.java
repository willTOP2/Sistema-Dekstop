package Model;

/**
 *
 * @author Willian
 */
public class Disciplina {
    
    private String nome;
    private int cod;

    public Disciplina(int cod) {
        this.nome = nome;
        this.cod = cod;
    }

    
    
    
    
 

    
    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public Disciplina() {
    }

    public int getCod() {
        return cod;
    }

    public void setCod(int cod) {
        this.cod = cod;
    }

   
    
    
    
}
